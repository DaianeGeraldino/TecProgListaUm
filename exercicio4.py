largura_retangulo = float(input("Digite a largura do retangulo"))
altura_retangulo = float(input("Digite a altura do retangulo"))

area_retangulo = largura_retangulo * altura_retangulo
perimetro_retangulo = (2*largura_retangulo)+(2*altura_retangulo)

print(f'a área do retangulo é igual a:  {area_retangulo:.2f}')
print(f'a perímetro do retangulo é igual a:  {perimetro_retangulo:.2f}')